package ObjectOrientedProgramming;

class MultipleClass {
    public static void main(String[] args) {
        System.out.println("All classes running");
    }
}

class A {
    public static void main(String[] args) {
        System.out.println("From class A");
    }
}
class B {
    public static void main(String[] args) {
        System.out.println("From class B");
    }
}
class C {
    public static void main(String[] args) {
        System.out.println("From class C");
    }
}
