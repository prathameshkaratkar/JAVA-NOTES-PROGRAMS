package Operator;

public class Compound1 {
    public static void main(String[] args) {
        byte b = 50;
        b += 5;
        short s = 125;
        s += 10;
        char ch = 'z';
        ch += 3;
        int i = 9251;
        i += 10;

        System.out.println(b);
        System.out.println(s);
        System.out.println(ch);
        System.out.println(i);
    }
}
